let dia = 7;
let animal = 'gato';

switch(dia){
    case 1:
        console.log('hoje é domingo');
    break;

    case 2:
        console.log('hoje é segunda');
    break;

    case 3:
        console.log('hoje é terça');
    break;

    case 4:
        console.log('hoje é quarta');
    break;

    case 5:
        console.log('hoje é quinta');
    break;

    case 6:
        console.log('hoje é sexta');
    break;

    case 7:
        console.log('hoje é sabado');
    break;

    default:
        break;
}

switch(animal){
    case 'cachorro':
        console.log('esse animal é vertebrado');
    break;

    case 'pepino do mar':
        console.log('esse animal é invertebrado');
    break;

    case 'gato':
        console.log('esse animal é onivoros');
    break;

    case 'galinha':
        console.log('esse animal é oviparos');
    break;

    case 'lagarta':
        console.log('esse animal é herbivoros');
    break;

    default:
        break;
}